<h1>ABOUT US</h1>



Technology today is complicated and constantly changing, yet comes with no instruction manual. To make things worse, customer support is at an all-time low. Computer Courage aims to fix this by providing personal, expert IT support and beautiful, hand crafted websites.
<br>
Commitment to the User
Computer Courage has always put the user first. Too many systems and companies put the focus on products or the bottom line, creating an environment where users are left without knowledgeable or friendly support and without easy to use websites. Our mission is to change that with friendly, local, professional support.

	<br>
Commitment to Our Team
Computer Courage grew from a one man operation in the basement of founder Adam Schwartz’s home. Today we support a full team of web and IT professionals and their families with job opportunities, health care, and a positive work environment. Our growing team of friendly experts is our most important asset.
Commitment to Professionalism
We aim to be more than just technically proficient and knowledgeable; we aim to provide professional, accountable solutions and service to our customers. You’ll see the difference at every step of the way – communication, project management, service, and follow up.
